package com.accolite.sleeppods.model;

public class UtilizationReport {

	int count;
	int dayy;

	public UtilizationReport() {
		super();
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getDayy() {
		return dayy;
	}

	public void setDayy(int dayy) {
		this.dayy = dayy;
	}

}
